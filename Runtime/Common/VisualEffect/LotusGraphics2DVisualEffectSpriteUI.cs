using System;
using UnityEngine;
using UnityEngine.UI;
//---------------------------------------------------------------------------------------------------------------------
using Lotus.Core;
//=====================================================================================================================
namespace Lotus
{
	namespace Graphics2D
	{
		//-------------------------------------------------------------------------------------------------------------
		//! \addtogroup Unity2DCommonVisualEffect
		/*@{*/
		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Визуальный эффекты связанные со спрайтом
		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		[Serializable]
		[AddComponentMenu("Lotus/Graphics2D/VisualEffect/Sprite UI")]
		public class LotusGraphics2DVisualEffectSpriteUI : LotusGraphics2DVisualEffectBase
		{
			#region ======================================= ДАННЫЕ ====================================================
			// Основные параметры
			[SerializeField]
			[LotusDisplayName(nameof(OriginalSprite))]
			internal Sprite mOriginalSprite;
			[SerializeField]
			[LotusDisplayName(nameof(TargetSprite))]
			internal Sprite mTargetSprite;

			// Служебные данные
			[NonSerialized]
			internal Image mUIImage;
			#endregion

			#region ======================================= СВОЙСТВА ==================================================
			//
			// ОСНОВНЫЕ ПАРАМЕТРЫ
			//
			/// <summary>
			/// Оригинальный спрайт элемента
			/// </summary>
			public Sprite OriginalSprite
			{
				get { return mOriginalSprite; }
				set
				{
					mOriginalSprite = value;
				}
			}

			/// <summary>
			/// Целевой спрайт элемента
			/// </summary>
			public Sprite TargetSprite
			{
				get { return mTargetSprite; }
				set
				{
					mTargetSprite = value;
				}
			}
			#endregion

			#region ======================================= ОБЩИЕ МЕТОДЫ ==============================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Установить оригинальное значение эффекта на основании текущего значения элемента
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			public override void SetOriginalValue()
			{
				if(mElement != null)
				{
					mUIImage = mElement.GetComponent<Image>();
				}
				if (mUIImage == null)
				{
					mUIImage = this.GetComponent<Image>();
				}
				if (mUIImage != null)
				{
					mOriginalSprite = mUIImage.sprite;
				}
				else
				{
					Debug.LogErrorFormat("Element <{0}> does not component Image", mElement);
				}
			}

			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Применение визуального эффекта активации элемента
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			public override void SetActivedEffect()
			{
				if(mUIImage != null)
				{
					mUIImage.sprite = mTargetSprite;
				}
			}

			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Применение визуального эффекта деактивации элемента
			/// </summary>
			//---------------------------------------------------------------------------------------------------------
			public override void SetDeactivedEffect()
			{
				if (mUIImage != null)
				{
					mUIImage.sprite = mOriginalSprite;
				}
			}
			#endregion
		}
		//-------------------------------------------------------------------------------------------------------------
		/*@}*/
		//-------------------------------------------------------------------------------------------------------------
	}
}
